
public class Person {
	String name;
	int year;
	String email;
	String program;
	String notes;
	String allergies;
	
	public Person(String name, int year, String email, String program, String allergies, String notes) {
		this.name = name;
		this.year = year;
		this.email = email;
		this.program = program;
		this.allergies = allergies;
		this.notes = notes;
	}
}
